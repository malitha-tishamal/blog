                });
                
                // Add click event to lightbox background
                lightbox.addEventListener('click', function(e) {
                    if (e.target === lightbox) {
                        document.body.removeChild(lightbox);
                    }
                });
                
                // Append elements
                lightbox.appendChild(lightboxImg);
                lightbox.appendChild(closeBtn);
                document.body.appendChild(lightbox);
                
                // Prevent body scroll when lightbox is open
                document.body.style.overflow = 'hidden';
                
                // Add ESC key support
                function handleEscKey(e) {
                    if (e.key === 'Escape') {
                        const lightbox = document.querySelector('.custom-lightbox');
                        if (lightbox) {
                            document.body.removeChild(lightbox);
                            document.body.style.overflow = '';
                        }
                        document.removeEventListener('keydown', handleEscKey);
                    }
                }
                
                document.addEventListener('keydown', handleEscKey);
                
                // Clean up when lightbox is removed
                lightbox.addEventListener('click', function(e) {
                    if (e.target === lightbox) {
                        document.body.style.overflow = '';
                    }
                });
            });
        });
        
        console.log('Custom lightbox initialized for', thumbnailImages.length, 'images');
        
        // Also initialize GLightbox for compatibility
        if (typeof GLightbox !== 'undefined') {
            const lightbox = GLightbox({
                selector: '.glightbox',
                touchNavigation: true,
                loop: true,
                autoplayVideos: true
            });
            console.log('GLightbox also initialized for compatibility');
        }
